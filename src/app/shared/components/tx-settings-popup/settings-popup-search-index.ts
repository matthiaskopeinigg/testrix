function optionLabels(
  options: readonly { readonly label: string }[],
): readonly string[] {
  return options.map((entry) => entry.label);
}

function flattenTerms(...groups: readonly (string | readonly string[])[]): readonly string[] {
  const terms: string[] = [];
  for (const group of groups) {
    if (typeof group === 'string') {
      terms.push(group);
      continue;
    }
    terms.push(...group);
  }
  return terms;
}

