const SETTINGS_SECTION_SEARCH_BLOBS: Record<SettingsPopupSection, string> = Object.fromEntries(
  (Object.entries(SETTINGS_SECTION_SEARCH_TERMS) as [SettingsPopupSection, readonly string[]][]).map(
    ([sectionId, terms]) => [sectionId, terms.join(' ').toLowerCase()],
  ),
) as Record<SettingsPopupSection, string>;

/**
 * Returns true when `query` matches sidebar metadata or any indexed option/label in the section.
 */
export function settingsSectionMatchesQuery(
  sectionId: SettingsPopupSection,
  query: string,
): boolean {
  const normalized = query.trim().toLowerCase();
  if (!normalized) {
    return true;
  }

  return SETTINGS_SECTION_SEARCH_BLOBS[sectionId].includes(normalized);
}
